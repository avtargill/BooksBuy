package com.example.bookbuy;

public class Books
{
    String id,title,author,publisher,language,edition,rating,yop,description;

    public Books(String id, String title, String author, String publisher, String language, String edition, String rating, String yop, String description) {
        this.id = id;
        this.title = title;
        this.author = author;
        this.publisher = publisher;
        this.language = language;
        this.edition = edition;
        this.rating = rating;
        this.yop = yop;
        this.description = description;
    }
}
